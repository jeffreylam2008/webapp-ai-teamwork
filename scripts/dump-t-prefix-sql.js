/**
 * Dump current `t_prefix` rows into `scripts/sql/001_create_t_prefix.sql`.
 *
 * Uses `src/data/db-config.json` as defaults, with optional env overrides:
 * DB_HOST, DB_PORT, DB_USER, DB_PASSWORD, DB_NAME
 */

const fs = require('fs');
const path = require('path');
const mysql = require('mysql2/promise');
const { resolveDbConfig } = require('./lib/resolve-db-config');

function getConfig() {
  const jsonPath = path.join(__dirname, '..', 'src', 'data', 'db-config.json');
  const base = JSON.parse(fs.readFileSync(jsonPath, 'utf8'));
  return resolveDbConfig(base);
}

function sqlString(v) {
  if (v === null || v === undefined) return 'NULL';
  return `'${String(v).replace(/\\/g, '\\\\').replace(/'/g, "''")}'`;
}

async function main() {
  const cfg = getConfig();
  const outPath = path.join(__dirname, 'sql', '001_create_t_prefix.sql');

  console.log('[dump-t_prefix] connecting', {
    host: cfg.host,
    port: cfg.port,
    user: cfg.user,
    database: cfg.database,
  });

  const conn = await mysql.createConnection({
    host: cfg.host,
    port: cfg.port,
    user: cfg.user,
    password: cfg.password,
    database: cfg.database,
  });

  try {
    // Some deployments include `prefix_ref` (stable reference code) in addition to `prefix`.
    // If the column doesn't exist, we fallback to prefix_ref = prefix in the generated SQL.
    let rows;
    try {
      const result = await conn.execute(
        'SELECT prefix, prefix_ref, `desc` AS prefix_desc, status FROM t_prefix ORDER BY prefix'
      );
      rows = result[0];
    } catch {
      const result = await conn.execute(
        'SELECT prefix, `desc` AS prefix_desc, status FROM t_prefix ORDER BY prefix'
      );
      rows = result[0];
    }
    const data = Array.isArray(rows) ? rows : [];
    if (data.length === 0) {
      throw new Error('t_prefix is empty; refusing to overwrite SQL file with empty seed.');
    }

    const valuesSql = data
      .map((r) => {
        const prefix = r.prefix;
        const prefix_ref = r.prefix_ref ?? r.prefix;
        const desc = r.prefix_desc;
        const status = r.status;
        return `  (${sqlString(prefix)}, ${sqlString(prefix_ref)}, ${sqlString(desc)}, ${Number(status) ? 1 : 0})`;
      })
      .join(',\n');

    const sql = `-- Prerequisite: transaction prefix master table (dumped from live DB)\n-- Generated by scripts/dump-t-prefix-sql.js\n--\n-- MySQL / MariaDB\n\nCREATE TABLE IF NOT EXISTS t_prefix (\n  uid INT NOT NULL AUTO_INCREMENT,\n  prefix VARCHAR(10) NOT NULL,\n  prefix_ref VARCHAR(10) NOT NULL,\n  \`desc\` VARCHAR(255) NOT NULL,\n  status TINYINT(1) NOT NULL DEFAULT 1,\n  create_date DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,\n  modify_date DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,\n  PRIMARY KEY (uid),\n  UNIQUE KEY uq_t_prefix_prefix (prefix),\n  UNIQUE KEY uq_t_prefix_prefix_ref (prefix_ref)\n);\n\nINSERT INTO t_prefix (prefix, prefix_ref, \`desc\`, status)\nVALUES\n${valuesSql}\nON DUPLICATE KEY UPDATE\n  prefix_ref = VALUES(prefix_ref),\n  \`desc\` = VALUES(\`desc\`),\n  status = VALUES(status);\n`;

    fs.mkdirSync(path.dirname(outPath), { recursive: true });
    fs.writeFileSync(outPath, sql, 'utf8');
    console.log(`[dump-t_prefix] wrote ${data.length} rows to ${outPath}`);
  } finally {
    await conn.end();
  }
}

main().catch((e) => {
  console.error('[dump-t_prefix] failed:', e && e.message ? e.message : e);
  process.exit(1);
});

